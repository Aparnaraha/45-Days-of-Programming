#include<iostream>
using namespace std;

int main() {
	// OVERFLOW - ONLY READ THE RIGHMOST BIT
	char ch = 600;
	cout << ch << endl;
	// PLACE 0 ON THE FIRST THREE BITS
	int a = 'd';
	cout << a << endl;
	// Storing float in int
	int b = 1.34;
	cout << b << endl;
	// int + char
	int ch1 = 'D' + 2;
	cout << ch1;
}